//Maths object : random()
//random():It returns random number between 0 (inclusive) and 1 (exclusive)
let r = Math.random();
console.log(r); //it will return a random value between 0 (inclusive) and 1 (exclusive)

let z = Math.random() * 2;
console.log("Random Number after *2 :", z);

let y = Math.random() * 2 + 1;
console.log("Random Number after * and + :", y);

let x = Math.floor(Math.random() * 2) + 5;
console.log("Random Number:", x);
