//Logical Operator:perform logical operations and return a boolean value
let a = 12;
let b = 6;
console.log(a > b && b < a); //Logical AND in this T+T=T otherwise false
console.log(a == b || b == a); //Logical OR in this F+F=F otherwise true
console.log(!(a > 40)); //Logical NOT it will make True to false or false to true.
