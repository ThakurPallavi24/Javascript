//Assignment Operator
let a = 8;
console.log("a+=2", (a += 2));
console.log(a);
console.log("a-=2", (a -= 2));
console.log(a);
console.log("a*=2", (a *= 2));
console.log(a);
console.log("a/=2", (a /= 2));
console.log(a);
console.log("a%=3", (a %= 3));
console.log(a);
console.log("a**=5", (a **= 5));
console.log(a);
