//Break: It is used to terminate the innermost loop or switch statement.
for (let i = 11; i <= 15; i++) {
  if (i === 14) {
    break;
  }
  console.log(i);
}

//Continue: It is used to skip the current iteration of the loop & proceed to the next itteration

for (let a = 1; a <= 7; a++) {
  if (a === 4) {
    continue;
  }
  console.log(a);
}
