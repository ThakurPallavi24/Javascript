//Loop allow us to iterate the part of the program repetedly until the condition is true.

//WAP in for loop to print 1 to 10.
console.log("For Loop : ");
for (let i = 1; i <= 10; i++) {
  console.log(i);
}

//WAP in while loop to print 11 to 20.

console.log("While Loop : ");

let a = 11;

while (a <= 20) {
  console.log(a);
  a++;
}

//WAP in do while loop to print 1 to 10.
console.log("Do While Loop : ");

let b = 1;
do {
  console.log(b);
  b++;
} while (b <= 10);
