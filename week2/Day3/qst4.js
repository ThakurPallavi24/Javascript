//Arrow function
const add = (a, b, c) => {
  return a + b + c;
};
let result = add(2, 6, 7);
console.log(result);

//Another example
const sub = (a, b) => {
  return a - b;
};
let result1 = sub(45, 9);
console.log(result1);

//Another example

const area = (l, b) => {
  return l * b;
};
let result2 = area(4, 7);
console.log(result2);
